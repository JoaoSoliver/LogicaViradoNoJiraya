package academy.devdojo.listaexercicios.iniciacao;

/*
 * Desenvolva uma rotina que dado um número inteiro qualquer calcule 
 * seu antecessor e seu sucessor.
 */

public class Exercicio18ExibeAntecessorSucessor {

	public static void main(String[] args) {
		
		int numero = 10;
		
		System.out.println("Antecessor de " + numero + " = " + (numero - 1));
		System.out.println("Sucessor de " + numero + " = " + (numero + 1));

	}

}
